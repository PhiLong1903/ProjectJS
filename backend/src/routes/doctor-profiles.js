const express = require("express");
const roles = require("../constants/roles");
const authHandler = require("../utils/authHandler");
const validateHandler = require("../utils/validateHandler");
const doctor_profiles = require("../controllers/doctor-profiles");
const doctor_profiles_validation = require("../utils/validators/doctor-profiles.validation");
exports.doctorRoutes = express.Router();
exports.doctorRoutes.use(authHandler.CheckLogin, authHandler.CheckRole(roles.ROLES.DOCTOR));
exports.doctorRoutes.get("/profile", doctor_profiles.getMyDoctorProfileController);
exports.doctorRoutes.patch("/profile", validateHandler.validateBody(doctor_profiles_validation.doctorProfileUpdateSchema), doctor_profiles.updateMyDoctorProfileController);
exports.doctorRoutes.get("/appointments", doctor_profiles.getMyDoctorAppointmentsController);
exports.doctorRoutes.patch("/appointments/:bookingId/status", validateHandler.validateBody(doctor_profiles_validation.doctorAppointmentStatusSchema), doctor_profiles.updateMyAppointmentStatusController);
exports.doctorRoutes.post("/appointments/:bookingId/prescription", validateHandler.validateBody(doctor_profiles_validation.doctorPrescriptionSchema), doctor_profiles.upsertMyPrescriptionController);
exports.doctorRoutes.get("/patients/:patientCode/records", doctor_profiles.getPatientRecordsByCodeController);
exports.doctorRoutes.post("/slots", validateHandler.validateBody(doctor_profiles_validation.doctorSlotSchema), doctor_profiles.createMyDoctorSlotController);
exports.doctorRoutes.delete("/slots/:slotId", doctor_profiles.deleteMyDoctorSlotController);
exports.doctorRoutes.get("/notifications", doctor_profiles.getMyDoctorNotificationsController);
exports.doctorRoutes.patch("/notifications/:notificationId/read", doctor_profiles.readMyDoctorNotificationController);
